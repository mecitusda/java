public class insan {
    private el el;
    private goz goz;
    private ayak ayak;

    public insan(ayak ayak,goz goz,el el) {
        this.setAyak(ayak);
        this.setEl(el);
        this.setGoz(goz);
    }


    public el getEl() {
        return el;
    }

    public void setEl(el el) {
        this.el = el;
    }

    public goz getGoz() {
        return goz;
    }

    public void setGoz(goz goz) {
        this.goz = goz;
    }

    public ayak getAyak() {
        return ayak;
    }

    public void setAyak(ayak ayak) {
        this.ayak = ayak;
    }

    
}
