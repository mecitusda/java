public class Physical {
    private int acceleration;
    private int agility;
    private int balance;
    private int jumping_Reach;
    private int natural_Fitness;
    private int pace;
    private int stamina;
    private int strength;

    public int getAcceleration() {
        return acceleration;
    }

    public void setAcceleration(int acceleration) {
        this.acceleration = acceleration;
    }

    public int getAgility() {
        return agility;
    }

    public void setAgility(int agility) {
        this.agility = agility;
    }

    public int getBalance() {
        return balance;
    }

    public void setBalance(int balance) {
        this.balance = balance;
    }

    public int getJumping_Reach() {
        return jumping_Reach;
    }

    public void setJumping_Reach(int jumping_Reach) {
        this.jumping_Reach = jumping_Reach;
    }

    public int getNatural_Fitness() {
        return natural_Fitness;
    }

    public void setNatural_Fitness(int natural_Fitness) {
        this.natural_Fitness = natural_Fitness;
    }

    public int getPace() {
        return pace;
    }

    public void setPace(int pace) {
        this.pace = pace;
    }

    public int getStamina() {
        return stamina;
    }

    public void setStamina(int stamina) {
        this.stamina = stamina;
    }

    public int getStrength() {
        return strength;
    }

    public void setStrength(int strength) {
        this.strength = strength;
    }


}
